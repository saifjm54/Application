package com.example.app.model;

// The POJO class - Product.java
public class Product {
    private String id;
    private String name;

    public Product() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
